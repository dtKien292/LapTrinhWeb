package model;

public class Order {
    private int id,uid;
    private String date;
    private Double totalmoney;

    public Order() {
    }

    public Order(int id, int uid, String date, Double totalmoney) {
        this.id = id;
        this.uid = uid;
        this.date = date;
        this.totalmoney = totalmoney;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getUid() {
        return uid;
    }

    public void setUid(int uid) {
        this.uid = uid;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public Double getTotalmoney() {
        return totalmoney;
    }

    public void setTotalmoney(Double totalmoney) {
        this.totalmoney = totalmoney;
    }
    
}
